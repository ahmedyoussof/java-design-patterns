package structural.composite;

public class Rectangle extends BaseShape{

    private int width;
    private int height;

    public Rectangle(int x, int y, String color, int width, int height) {
        super(x, y, color);
        this.width = width;
        this.height = height;
    }

    @Override
    public int getWidth() {
        return width;
    }

    @Override
    public int getHeight() {
        return height;
    }

    @Override
    public void paint(Graphics graphics) {
        graphics.drawRectangle(getX(), getY(), getWidth(), getHeight(), getColor());
    }
}
